#include<iostream>
using namespace std;
int main()
{
    int a ;
    float b;
    char c;
    bool r;
    short int si;
    long int li;
    cout<<"size of integer "<<sizeof(a)<<endl;
    cout<<"size of float "<<sizeof(b)<<endl;
    cout<<"size of char "<<sizeof(c)<<endl;
    cout<<"sizeof of bool "<<sizeof(r)<<endl;
    cout<<"sizeof of short int "<<sizeof(si)<<endl;
    cout<<"sizeof of long int "<<sizeof(li)<<endl;
    return 0;
}
